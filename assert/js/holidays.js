// 辅助函数：判断某天是否为休息日
function isRestDay(date) {
  const dateStr =
    typeof date === "string"
      ? date
      : `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, "0")}-${String(date.getDate()).padStart(2, "0")}`
  return restDays.includes(dateStr)
}

// 辅助函数：判断某天是否为调休工作日
function isWorkDay(date) {
  const dateStr =
    typeof date === "string"
      ? date
      : `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, "0")}-${String(date.getDate()).padStart(2, "0")}`
  return workDays.includes(dateStr)
}

